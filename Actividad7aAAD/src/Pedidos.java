import java.util.Date;


public class Pedidos {

	private int id = 0;
	private String fecha = null;
	
	public Pedidos(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
}
